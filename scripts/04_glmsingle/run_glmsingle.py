#!/usr/bin/env python
# -*- coding: utf-8 -*-
"""
Run Python GLMsingle on a prepared NVD GLMsingle bundle.

The bundle should be generated by `build_glmsingle_bundle.py`. Data are loaded as
a list of arrays with shape grayordinates x timepoints; designs are loaded as a
list of dense arrays with shape timepoints x conditions.
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Any

import numpy as np
import pandas as pd
from scipy import sparse as sp


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Run GLMsingle on a prepared bundle.")
    parser.add_argument("--bundle-dir", required=True, type=Path, help="Input bundle directory.")
    parser.add_argument("--output-dir", required=True, type=Path, help="GLMsingle output directory.")
    parser.add_argument("--figure-dir", required=True, type=Path, help="GLMsingle figure directory.")
    parser.add_argument("--chunklen", type=int, default=100000, help="GLMsingle chunk length.")
    parser.add_argument("--wantlibrary", type=int, default=1)
    parser.add_argument("--wantglmdenoise", type=int, default=1)
    parser.add_argument("--wantfracridge", type=int, default=1)
    parser.add_argument("--session-column", default="repeat_index", help="run_table column used for sessionindicator. Use 'none' to omit.")
    parser.add_argument("--mmap-data", action="store_true", help="Memory-map run data arrays.")
    return parser.parse_args()


def resolve_path(path_value: Any, base_dir: Path) -> Path:
    path = Path(str(path_value))
    return path if path.is_absolute() else base_dir / path


def load_subject_bundle(bundle_dir: Path, dense_design: bool = True, mmap_data: bool = False):
    """Load a GLMsingle bundle produced by build_glmsingle_bundle.py."""
    with open(bundle_dir / "meta.json", "r", encoding="utf-8") as f:
        meta = json.load(f)
    run_table = pd.read_csv(bundle_dir / "run_table.csv").sort_values("run_index_1based").reset_index(drop=True)
    condition_ids = np.load(bundle_dir / "condition_ids.npy", allow_pickle=True)

    data = []
    design = []
    mmap_mode = "r" if mmap_data else None
    for _, row in run_table.iterrows():
        data_file = resolve_path(row["data_file"], bundle_dir)
        design_file = resolve_path(row["design_file"], bundle_dir)
        data.append(np.load(data_file, mmap_mode=mmap_mode))
        matrix = sp.load_npz(design_file).tocsc()
        design.append(matrix.toarray().astype(np.float32) if dense_design else matrix)

    return data, design, float(meta["tr"]), float(meta["stimdur"]), condition_ids, run_table, meta


def main() -> None:
    args = parse_args()
    args.output_dir.mkdir(parents=True, exist_ok=True)
    args.figure_dir.mkdir(parents=True, exist_ok=True)

    data, design, tr, stimdur, condition_ids, run_table, meta = load_subject_bundle(
        args.bundle_dir, dense_design=True, mmap_data=args.mmap_data
    )

    params = {
        "wantlibrary": int(args.wantlibrary),
        "wantglmdenoise": int(args.wantglmdenoise),
        "wantfracridge": int(args.wantfracridge),
        "chunklen": int(args.chunklen),
    }

    if args.session_column.lower() != "none":
        if args.session_column not in run_table.columns:
            raise ValueError(f"Session column not found in run_table: {args.session_column}")
        params["sessionindicator"] = run_table[args.session_column].to_numpy(dtype=int).reshape(1, -1)

    config = {
        "bundle_dir": str(args.bundle_dir),
        "output_dir": str(args.output_dir),
        "figure_dir": str(args.figure_dir),
        "tr": tr,
        "stimdur": stimdur,
        "n_runs": len(data),
        "n_conditions": int(len(condition_ids)),
        "params": {key: (value.tolist() if isinstance(value, np.ndarray) else value) for key, value in params.items()},
        "bundle_meta": meta,
    }
    with open(args.output_dir / "glmsingle_run_config.json", "w", encoding="utf-8") as f:
        json.dump(config, f, ensure_ascii=False, indent=2)

    print(f"[INFO] Loaded {len(data)} runs; first data shape={data[0].shape}; first design shape={design[0].shape}")
    print(f"[INFO] TR={tr}, stimdur={stimdur}, n_conditions={len(condition_ids)}")

    try:
        from glmsingle.glmsingle import GLM_single
    except ImportError as exc:
        raise ImportError(
            "The Python GLMsingle package is required. Install GLMsingle before running this script."
        ) from exc

    glmsingle = GLM_single(params)
    glmsingle.fit(
        design=design,
        data=data,
        stimdur=stimdur,
        tr=tr,
        outputdir=str(args.output_dir),
        figuredir=str(args.figure_dir),
    )
    print(f"[DONE] GLMsingle outputs written to {args.output_dir}")


if __name__ == "__main__":
    main()

