#!/usr/bin/env python
# -*- coding: utf-8 -*-
"""
Export GLMsingle beta estimates to CIFTI dscalar files.

The script supports two GLMsingle beta layouts:

1. trialwise: one beta row/column per trial in `trial_table.csv` order;
2. conditionwise: one beta row/column per condition column.

Outputs can be written for the full 91k grayordinate space and, optionally, for
visual ROIs defined by a `roimap_91k_visual_rois.mat` file.
"""

from __future__ import annotations

import argparse
import gc
import re
import shutil
import traceback
from pathlib import Path
from typing import Any

import nibabel as nib
import numpy as np
import pandas as pd
import scipy.io as sio
from nibabel.cifti2 import Cifti2Header, Cifti2Image
from nibabel.cifti2.cifti2_axes import BrainModelAxis, ScalarAxis


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Export GLMsingle betas to CIFTI dscalar files.")
    parser.add_argument("--beta-dir", required=True, type=Path, help="Directory containing sub-*_TYPED_FITHRF_GLMDENOISE_RR.npy files.")
    parser.add_argument("--bundle-root", required=True, type=Path, help="Root containing sub-xxx_glmsingle_33runs_bundle directories.")
    parser.add_argument("--output-root", required=True, type=Path, help="Output root directory.")
    parser.add_argument("--template-cifti", type=Path, default=None, help="91k CIFTI template. If omitted, the script searches --template-search-dir.")
    parser.add_argument("--template-search-dir", type=Path, default=None, help="Directory searched for a 91k CIFTI template when --template-cifti is omitted.")
    parser.add_argument("--roi-mat", type=Path, default=None, help="Optional roimap_91k_visual_rois.mat file.")
    parser.add_argument("--roi-id-table", type=Path, default=None, help="Optional visual ROI ID table CSV.")
    parser.add_argument("--betas-key", default="betasmd", help="Key to read from GLMsingle TYPED dict.")
    parser.add_argument("--expected-grayordinates", type=int, default=91282)
    parser.add_argument("--write-full-91k", action="store_true", default=True)
    parser.add_argument("--no-full-91k", dest="write_full_91k", action="store_false")
    parser.add_argument("--write-visual-rois", action="store_true", help="Write visual ROI subsets. Requires --roi-mat and --roi-id-table.")
    parser.add_argument("--write-raw-by-run", action="store_true", default=True)
    parser.add_argument("--no-raw-by-run", dest="write_raw_by_run", action="store_false")
    parser.add_argument("--write-avg-all", action="store_true", default=True)
    parser.add_argument("--no-avg-all", dest="write_avg_all", action="store_false")
    parser.add_argument("--write-roi-mean", action="store_true", help="Also write ROI-mean CSV/NPY outputs for visual ROIs.")
    parser.add_argument("--skip-existing", action="store_true", help="Skip existing dscalar files but still write metadata CSV files.")
    return parser.parse_args()


def mkdirs(args: argparse.Namespace) -> None:
    """Create all output directories used by this script."""
    dirs = [
        args.output_root / "full91k" / "raw_by_run",
        args.output_root / "full91k" / "avg_all",
        args.output_root / "visual_rois" / "raw_by_run",
        args.output_root / "visual_rois" / "avg_all",
        args.output_root / "visual_rois" / "roi_mean_raw_by_run",
        args.output_root / "visual_rois" / "roi_mean_avg_all",
        args.output_root / "metadata",
    ]
    for path in dirs:
        path.mkdir(parents=True, exist_ok=True)


def format_sub(value: Any) -> str:
    text = str(value)
    if text.startswith("sub-"):
        return text
    try:
        return f"sub-{int(float(text)):03d}"
    except Exception:
        return f"sub-{text}"


def format_ses(value: Any) -> str:
    text = str(value)
    if text.startswith("ses-"):
        return text
    try:
        return f"ses-{int(float(text)):03d}"
    except Exception:
        return f"ses-{text}"


def format_task(value: Any) -> str:
    text = str(value)
    return text if text.startswith("task-") else f"task-{text}"


def parse_sub_from_beta_path(beta_path: Path) -> str | None:
    match = re.match(r"(sub-[^_]+)_TYPED_FITHRF_GLMDENOISE_RR\.npy$", beta_path.name)
    return match.group(1) if match else None


def find_trial_table(bundle_root: Path, sub_label: str) -> Path | None:
    """Find a subject trial_table.csv generated by build_glmsingle_bundle.py."""
    candidates = [
        bundle_root / f"{sub_label}_glmsingle_33runs_bundle" / "trial_table.csv",
        bundle_root / f"{sub_label}_glmsingle_33runs_bundle_trial_table.csv",
        bundle_root / f"{sub_label}_trial_table.csv",
    ]
    for candidate in candidates:
        if candidate.exists():
            return candidate
    hits = sorted(bundle_root.rglob(f"{sub_label}*trial_table*.csv"))
    return hits[0] if hits else None


def find_template_cifti(args: argparse.Namespace) -> Path:
    if args.template_cifti is not None:
        if not args.template_cifti.exists():
            raise FileNotFoundError(f"Template CIFTI does not exist: {args.template_cifti}")
        return args.template_cifti

    roots = [args.template_search_dir, args.bundle_root, args.beta_dir]
    patterns = ["**/*_space-fsLR_den-91k_bold.dtseries.nii", "**/*.dtseries.nii", "**/*.dscalar.nii", "**/*.dlabel.nii"]
    for root in roots:
        if root is None or not root.exists():
            continue
        for pattern in patterns:
            hits = sorted(root.glob(pattern))
            if hits:
                return hits[0]
    raise FileNotFoundError("No CIFTI template found. Provide --template-cifti.")


def get_brain_model_axis(template_path: Path, expected_grayordinates: int) -> BrainModelAxis:
    img = nib.load(str(template_path))
    axes = []
    for axis_index in range(img.ndim):
        axis = img.header.get_axis(axis_index)
        axes.append(axis)
        if isinstance(axis, BrainModelAxis) and len(axis) == expected_grayordinates:
            return axis
    details = "\n".join(f"axis {i}: {type(axis).__name__}, len={len(axis)}" for i, axis in enumerate(axes))
    raise ValueError(f"Could not find a BrainModelAxis with length {expected_grayordinates}.\n{details}")


def safe_scalar_names(names: list[str]) -> list[str]:
    out = []
    for index, name in enumerate(names):
        text = str(name).replace(" ", "_")
        if len(text) > 120:
            text = text[:110] + f"_{index:06d}"
        out.append(text)
    return out


def write_dscalar(data_2d: np.ndarray, scalar_names: list[str], brain_axis: BrainModelAxis, out_path: Path) -> None:
    """Write a 2D array as a CIFTI dscalar image."""
    out_path.parent.mkdir(parents=True, exist_ok=True)
    data_2d = np.asarray(data_2d, dtype=np.float32)
    if data_2d.ndim != 2:
        raise ValueError(f"data_2d must be 2D, got shape={data_2d.shape}")
    if data_2d.shape[0] != len(scalar_names):
        raise ValueError(f"Number of rows does not match scalar names: {data_2d.shape[0]} vs {len(scalar_names)}")
    if data_2d.shape[1] != len(brain_axis):
        raise ValueError(f"Number of columns does not match brain axis length: {data_2d.shape[1]} vs {len(brain_axis)}")
    scalar_axis = ScalarAxis(safe_scalar_names(scalar_names))
    img = Cifti2Image(dataobj=data_2d, header=Cifti2Header.from_axes((scalar_axis, brain_axis)))
    img.update_headers()
    nib.save(img, str(out_path))


def load_betas(beta_path: Path, key: str) -> np.ndarray:
    obj = np.load(str(beta_path), allow_pickle=True)
    if isinstance(obj, np.ndarray) and obj.dtype == object and obj.shape == ():
        obj = obj.item()
    if isinstance(obj, dict):
        if key in obj:
            return obj[key]
        lower = {str(k).lower(): k for k in obj.keys()}
        if key.lower() in lower:
            return obj[lower[key.lower()]]
        raise KeyError(f"Key '{key}' not found in {beta_path.name}; available keys: {list(obj.keys())}")
    if isinstance(obj, np.ndarray):
        return obj
    raise TypeError(f"Unsupported content type in {beta_path}: {type(obj)}")


def load_trial_table(path: Path) -> pd.DataFrame:
    table = pd.read_csv(path)
    required = ["run_index_1based", "local_trial_index", "condition_id", "condition_col_0based", "sub", "ses", "task"]
    missing = [column for column in required if column not in table.columns]
    if missing:
        raise ValueError(f"{path.name} is missing columns: {missing}")
    table = table.copy()
    table["_trial_index_0based"] = np.arange(len(table), dtype=np.int64)
    table["sub_label"] = table["sub"].map(format_sub)
    table["ses_label"] = table["ses"].map(format_ses)
    table["task_label"] = table["task"].map(format_task)
    table["run_stem"] = table["sub_label"] + "_" + table["ses_label"] + "_" + table["task_label"]
    return table


def standardize_betas_to_rows_by_grayordinates(betas: np.ndarray, trial_table: pd.DataFrame, n_gray: int) -> tuple[np.ndarray, str]:
    """Return a n_beta x n_gray matrix and beta-indexing mode."""
    arr = np.asarray(betas)
    n_trials = len(trial_table)
    max_condition_col = int(trial_table["condition_col_0based"].max()) + 1
    n_unique_conditions = int(trial_table["condition_id"].nunique())
    candidate_beta_lengths = [n_trials, max_condition_col, n_unique_conditions]

    if arr.ndim == 2:
        if arr.shape[0] == n_gray and arr.shape[1] in candidate_beta_lengths:
            arr2 = arr.T
        elif arr.shape[1] == n_gray and arr.shape[0] in candidate_beta_lengths:
            arr2 = arr
        elif arr.shape[0] == n_gray and arr.shape[1] >= max_condition_col:
            arr2 = arr.T
        elif arr.shape[1] == n_gray and arr.shape[0] >= max_condition_col:
            arr2 = arr
        else:
            raise ValueError(
                f"Cannot infer beta orientation. shape={arr.shape}, n_gray={n_gray}, "
                f"n_trials={n_trials}, max_condition_col={max_condition_col}"
            )
    else:
        candidates = [axis for axis, size in enumerate(arr.shape) if size in candidate_beta_lengths]
        if not candidates:
            candidates = [arr.ndim - 1]

        def rank(axis: int) -> int:
            size = arr.shape[axis]
            if size == n_trials:
                return 0
            if size == max_condition_col:
                return 1
            if size == n_unique_conditions:
                return 2
            return 3

        beta_axis = sorted(candidates, key=rank)[0]
        arr2 = np.moveaxis(arr, beta_axis, 0).reshape(arr.shape[beta_axis], -1)
        if arr2.shape[1] != n_gray and arr2.shape[0] == n_gray:
            arr2 = arr2.T
        if arr2.shape[1] != n_gray:
            raise ValueError(f"After reshaping, expected {n_gray} grayordinates but got {arr2.shape}")

    n_beta = arr2.shape[0]
    if n_beta == n_trials:
        mode = "trialwise"
    elif n_beta >= max_condition_col:
        mode = "conditionwise"
    else:
        raise ValueError(f"Cannot map beta rows to trial_table: n_beta={n_beta}, n_trials={n_trials}")
    return arr2.astype(np.float32, copy=False), mode


def raw_betas_for_run(betas2d: np.ndarray, run_df: pd.DataFrame, mode: str) -> np.ndarray:
    if mode == "trialwise":
        indices = run_df["_trial_index_0based"].to_numpy(dtype=np.int64)
    elif mode == "conditionwise":
        indices = run_df["condition_col_0based"].to_numpy(dtype=np.int64)
    else:
        raise ValueError(mode)
    if indices.min() < 0 or indices.max() >= betas2d.shape[0]:
        raise IndexError(f"Beta index out of range: min={indices.min()}, max={indices.max()}, n_rows={betas2d.shape[0]}")
    return betas2d[indices, :]


def make_raw_index_table(run_df: pd.DataFrame) -> pd.DataFrame:
    keep_cols = [
        "_trial_index_0based", "run_index_1based", "local_trial_index", "orig_event_index", "condition_id",
        "condition_col_0based", "condition_col_1based", "onset_sec", "onset_tr_float", "onset_tr_0based",
        "onset_tr_1based", "onset_error_sec", "onset_binning", "sub_label", "ses_label", "task_label",
        "repeat_index", "task_index",
    ]
    keep_cols = [column for column in keep_cols if column in run_df.columns]
    out = run_df[keep_cols].copy()
    out.insert(0, "scalar_index_0based", np.arange(len(out), dtype=np.int64))
    return out


def sanitize_condition_id(value: Any) -> str:
    return re.sub(r"[^A-Za-z0-9_.-]+", "-", str(value))


def make_all_avg_by_condition(betas2d: np.ndarray, table: pd.DataFrame, mode: str) -> tuple[np.ndarray, pd.DataFrame]:
    """Average betas by condition across all runs/repeats."""
    condition_meta = (
        table[["condition_id", "condition_col_0based"]]
        .drop_duplicates()
        .sort_values("condition_col_0based", kind="mergesort")
        .reset_index(drop=True)
    )
    avg_rows = []
    condition_rows = []
    for scalar_index, meta in condition_meta.iterrows():
        condition_id = meta["condition_id"]
        condition_col = int(meta["condition_col_0based"])
        group = table.loc[table["condition_id"] == condition_id].copy()
        group = group.sort_values(["run_index_1based", "local_trial_index", "_trial_index_0based"], kind="mergesort")
        if mode == "trialwise":
            beta_rows = group["_trial_index_0based"].to_numpy(dtype=np.int64)
            avg_vec = np.nanmean(betas2d[beta_rows, :], axis=0)
            source_beta_rows = ";".join(map(str, beta_rows.tolist()))
            n_betas_averaged = len(group)
        else:
            avg_vec = betas2d[condition_col, :]
            source_beta_rows = str(condition_col)
            n_betas_averaged = 1
        first = group.iloc[0]
        condition_rows.append(
            {
                "scalar_index_0based": int(scalar_index),
                "condition_id": condition_id,
                "condition_col_0based": int(condition_col),
                "condition_col_1based": int(condition_col + 1),
                "n_trials_presented": int(len(group)),
                "n_betas_averaged": int(n_betas_averaged),
                "beta_index_mode": mode,
                "source_beta_rows_0based": source_beta_rows,
                "trial_indices_0based": ";".join(group["_trial_index_0based"].astype(str).tolist()),
                "run_indices_1based": ";".join(group["run_index_1based"].astype(str).tolist()),
                "local_trial_indices": ";".join(group["local_trial_index"].astype(str).tolist()),
                "run_stems": ";".join(group["run_stem"].astype(str).tolist()),
                "first_trial_index_0based": int(first["_trial_index_0based"]),
                "first_run_index_1based": int(first["run_index_1based"]),
                "first_local_trial_index": int(first["local_trial_index"]),
                "first_sub": first["sub_label"],
                "first_ses": first["ses_label"],
                "first_task": first["task_label"],
            }
        )
        avg_rows.append(avg_vec.astype(np.float32, copy=False))
    return np.vstack(avg_rows).astype(np.float32, copy=False), pd.DataFrame(condition_rows)


def load_visual_roi_map(roi_mat: Path, roi_id_table_path: Path, n_gray: int):
    mat = sio.loadmat(str(roi_mat))
    if "roimap" not in mat:
        raise KeyError(f"'roimap' not found in {roi_mat}")
    roimap = np.asarray(mat["roimap"]).squeeze()
    if roimap.shape[0] != n_gray:
        raise ValueError(f"roimap length={roimap.shape[0]}, expected {n_gray}")
    roi_id_table = pd.read_csv(roi_id_table_path)
    if not {"roi_id", "roi_name"}.issubset(roi_id_table.columns):
        raise ValueError(f"{roi_id_table_path.name} must contain roi_id and roi_name columns")
    id_to_name = dict(zip(roi_id_table["roi_id"].astype(int), roi_id_table["roi_name"].astype(str)))
    visual_idx = np.flatnonzero(roimap > 0).astype(np.int64)
    visual_roi_ids = roimap[visual_idx].astype(int)
    visual_columns = pd.DataFrame(
        {
            "visual_col_0based": np.arange(len(visual_idx), dtype=np.int64),
            "grayordinate_91k_0based": visual_idx,
            "grayordinate_91k_1based": visual_idx + 1,
            "roi_id": visual_roi_ids,
            "roi_name": [id_to_name.get(int(value), f"ROI_{int(value)}") for value in visual_roi_ids],
        }
    )
    return roimap.astype(np.int16), roi_id_table, visual_idx, visual_columns


def compute_roi_mean(data_2d_visual: np.ndarray, visual_columns: pd.DataFrame, roi_id_table: pd.DataFrame) -> pd.DataFrame:
    out = {}
    roi_ids = visual_columns["roi_id"].to_numpy(dtype=int)
    for _, row in roi_id_table.iterrows():
        roi_id = int(row["roi_id"])
        roi_name = str(row["roi_name"])
        cols = np.flatnonzero(roi_ids == roi_id)
        out[roi_name] = np.nanmean(data_2d_visual[:, cols], axis=1) if len(cols) else np.full((data_2d_visual.shape[0],), np.nan)
    return pd.DataFrame(out)


def save_roi_mean(prefix_path: Path, data_2d_visual: np.ndarray, index_df: pd.DataFrame, visual_columns: pd.DataFrame, roi_id_table: pd.DataFrame) -> None:
    roi_mean_df = compute_roi_mean(data_2d_visual, visual_columns, roi_id_table)
    out_df = pd.concat([index_df.reset_index(drop=True), roi_mean_df.reset_index(drop=True)], axis=1)
    out_df.to_csv(prefix_path.with_suffix(".csv"), index=False, encoding="utf-8-sig")
    np.save(prefix_path.with_suffix(".npy"), roi_mean_df.to_numpy(dtype=np.float32))


def copy_once(src: Path, dst: Path) -> None:
    dst.parent.mkdir(parents=True, exist_ok=True)
    if not dst.exists():
        shutil.copy2(src, dst)


def process_one_subject(beta_path: Path, args: argparse.Namespace, brain_axis: BrainModelAxis, visual_axis, visual_idx, visual_columns, roi_id_table) -> list[dict]:
    sub_label = parse_sub_from_beta_path(beta_path)
    if sub_label is None:
        print(f"[SKIP] Cannot parse subject ID from {beta_path.name}")
        return []
    trial_table_path = find_trial_table(args.bundle_root, sub_label)
    if trial_table_path is None:
        print(f"[SKIP] Missing trial_table for {sub_label}")
        return []

    table = load_trial_table(trial_table_path)
    betas = load_betas(beta_path, args.betas_key)
    betas2d, mode = standardize_betas_to_rows_by_grayordinates(betas, table, args.expected_grayordinates)
    print(f"[SUB] {sub_label}: betas2d={betas2d.shape}, mode={mode}, trials={len(table)}")

    results = []
    table_sorted = table.sort_values(["run_index_1based", "local_trial_index", "_trial_index_0based"], kind="mergesort")

    if args.write_raw_by_run:
        for run_index, run_df in table_sorted.groupby("run_index_1based", sort=True):
            run_df = run_df.copy()
            stems = run_df["run_stem"].unique()
            if len(stems) != 1:
                raise ValueError(f"{sub_label} run {run_index}: multiple run stems: {stems}")
            stem = stems[0]
            raw_run = raw_betas_for_run(betas2d, run_df, mode)
            raw_index = make_raw_index_table(run_df)
            raw_names = [
                f"{stem}_trial-{int(row.local_trial_index):03d}_cond-{sanitize_condition_id(row.condition_id)}"
                for row in run_df.itertuples(index=False)
            ]
            if args.write_full_91k:
                out_dscalar = args.output_root / "full91k" / "raw_by_run" / f"{stem}_betasmd_raw.dscalar.nii"
                out_csv = args.output_root / "full91k" / "raw_by_run" / f"{stem}_trials.csv"
                if (not args.skip_existing) or (not out_dscalar.exists()):
                    write_dscalar(raw_run, raw_names, brain_axis, out_dscalar)
                raw_index.to_csv(out_csv, index=False, encoding="utf-8-sig")
            if args.write_visual_rois:
                raw_vis = raw_run[:, visual_idx]
                out_dscalar = args.output_root / "visual_rois" / "raw_by_run" / f"{stem}_visualROIs_betasmd_raw.dscalar.nii"
                out_csv = args.output_root / "visual_rois" / "raw_by_run" / f"{stem}_visualROIs_trials.csv"
                if (not args.skip_existing) or (not out_dscalar.exists()):
                    write_dscalar(raw_vis, raw_names, visual_axis, out_dscalar)
                raw_index.to_csv(out_csv, index=False, encoding="utf-8-sig")
                if args.write_roi_mean:
                    prefix = args.output_root / "visual_rois" / "roi_mean_raw_by_run" / f"{stem}_visualROIs_roiMean_raw"
                    save_roi_mean(prefix, raw_vis, raw_index, visual_columns, roi_id_table)
                del raw_vis
            results.append(
                {
                    "sub": sub_label,
                    "output_type": "raw_by_run",
                    "run_index_1based": int(run_index),
                    "output_stem": stem,
                    "n_scalars": int(raw_run.shape[0]),
                    "n_grayordinates_full": int(raw_run.shape[1]),
                    "n_visual_grayordinates": int(len(visual_idx)) if visual_idx is not None else 0,
                    "beta_index_mode": mode,
                }
            )
            print(f"[DONE] raw_by_run {stem}: {raw_run.shape}")
            del raw_run
            gc.collect()

    if args.write_avg_all:
        avg_all, conditions = make_all_avg_by_condition(betas2d, table_sorted, mode)
        avg_stem = f"{sub_label}_allRuns"
        avg_names = [
            f"{sub_label}_cond-{sanitize_condition_id(row.condition_id)}_n-{int(row.n_trials_presented)}"
            for row in conditions.itertuples(index=False)
        ]
        if args.write_full_91k:
            out_dscalar = args.output_root / "full91k" / "avg_all" / f"{avg_stem}_betasmd_avg.dscalar.nii"
            out_csv = args.output_root / "full91k" / "avg_all" / f"{avg_stem}_conditions.csv"
            if (not args.skip_existing) or (not out_dscalar.exists()):
                write_dscalar(avg_all, avg_names, brain_axis, out_dscalar)
            conditions.to_csv(out_csv, index=False, encoding="utf-8-sig")
        if args.write_visual_rois:
            avg_vis = avg_all[:, visual_idx]
            out_dscalar = args.output_root / "visual_rois" / "avg_all" / f"{avg_stem}_visualROIs_betasmd_avg.dscalar.nii"
            out_csv = args.output_root / "visual_rois" / "avg_all" / f"{avg_stem}_visualROIs_conditions.csv"
            if (not args.skip_existing) or (not out_dscalar.exists()):
                write_dscalar(avg_vis, avg_names, visual_axis, out_dscalar)
            conditions.to_csv(out_csv, index=False, encoding="utf-8-sig")
            if args.write_roi_mean:
                prefix = args.output_root / "visual_rois" / "roi_mean_avg_all" / f"{avg_stem}_visualROIs_roiMean_avg"
                save_roi_mean(prefix, avg_vis, conditions, visual_columns, roi_id_table)
            del avg_vis
        results.append(
            {
                "sub": sub_label,
                "output_type": "avg_all",
                "run_index_1based": "ALL",
                "output_stem": avg_stem,
                "n_scalars": int(avg_all.shape[0]),
                "n_grayordinates_full": int(avg_all.shape[1]),
                "n_visual_grayordinates": int(len(visual_idx)) if visual_idx is not None else 0,
                "beta_index_mode": mode,
                "n_unique_conditions": int(conditions["condition_id"].nunique()),
                "n_trials_total": int(len(table)),
            }
        )
        print(f"[DONE] avg_all {avg_stem}: {avg_all.shape}")
        del avg_all, conditions

    del betas, betas2d
    gc.collect()
    return results


def main() -> None:
    args = parse_args()
    mkdirs(args)
    template_path = find_template_cifti(args)
    brain_axis = get_brain_model_axis(template_path, args.expected_grayordinates)
    print(f"[TEMPLATE] {template_path}")

    visual_axis = None
    visual_idx = None
    visual_columns = None
    roi_id_table = None
    if args.write_visual_rois:
        if args.roi_mat is None or args.roi_id_table is None:
            raise ValueError("--write-visual-rois requires --roi-mat and --roi-id-table")
        roimap, roi_id_table, visual_idx, visual_columns = load_visual_roi_map(args.roi_mat, args.roi_id_table, args.expected_grayordinates)
        visual_axis = brain_axis[visual_idx]
        visual_columns.to_csv(args.output_root / "visual_rois" / "visual_columns.csv", index=False, encoding="utf-8-sig")
        roi_id_table.to_csv(args.output_root / "visual_rois" / "visual_roi_id_table.csv", index=False, encoding="utf-8-sig")
        np.save(args.output_root / "visual_rois" / "visual_grayordinate_91k_indices_0based.npy", visual_idx)
        np.save(args.output_root / "visual_rois" / "roimap_91k_visual_rois.npy", roimap)
        copy_once(args.roi_mat, args.output_root / "metadata" / args.roi_mat.name)
        copy_once(args.roi_id_table, args.output_root / "metadata" / args.roi_id_table.name)
    else:
        visual_idx = np.array([], dtype=np.int64)

    beta_files = sorted(args.beta_dir.glob("sub-*_TYPED_FITHRF_GLMDENOISE_RR.npy"))
    if not beta_files:
        raise FileNotFoundError(f"No GLMsingle beta files found in {args.beta_dir}")

    all_results = []
    failed = []
    for beta_path in beta_files:
        try:
            all_results.extend(process_one_subject(beta_path, args, brain_axis, visual_axis, visual_idx, visual_columns, roi_id_table))
        except Exception as exc:
            sub = parse_sub_from_beta_path(beta_path) or beta_path.stem
            failed.append({"sub": sub, "beta_path": str(beta_path), "error": repr(exc)})
            print(f"[ERROR] {sub}: {repr(exc)}")
            traceback.print_exc()

    if all_results:
        pd.DataFrame(all_results).to_csv(args.output_root / "extraction_summary.csv", index=False, encoding="utf-8-sig")
    if failed:
        pd.DataFrame(failed).to_csv(args.output_root / "failed_subjects.csv", index=False, encoding="utf-8-sig")
    print(f"[FINISHED] Outputs written to {args.output_root}")


if __name__ == "__main__":
    main()

